'''the process of converting one data type to another
types of typecasting :
    1.implicit typecasting(automatic)
    
      - automatically done by python interpreter.
      - seen when we perform operations(expressions, assignments) with different datatypes.
      - lower dataype to bigger datatype.
      - to avoid data loss.
      
    2.explicit typecasting(external)
    
      - manually done by user.
      - we make use of builtin functions.
      - to avoid unexpected behaviour.'''

